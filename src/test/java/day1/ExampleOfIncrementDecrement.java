package day1;

public class ExampleOfIncrementDecrement {
    public static void main(String[] args) {
        System.out.println(1);
        System.out.println(2);
        for (int i=1;i<=10;i++){
            System.out.println(i);

        }
        System.out.println("Decrement");
        for (int i =10;i>=1;i--){
            System.out.println(i);
        }
    }
}
